export function getTranslation(lang: Language, key: keyof typeof translations.en, vars?: Record<string, string>): string {
  const translationMap = translations[lang as keyof typeof translations];
  let text = (translationMap[key as keyof typeof translationMap] as string) || translations.en[key];
  
  if (vars) {
    Object.entries(vars).forEach(([key, value]) => {
      text = text.replace(`{{${key}}}`, value);
    });
  }
  
  return text;
}
